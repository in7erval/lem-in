#include "libft.h"

void	ft_lstrev(t_list **list)
{
	t_list *cur;
	t_list *prev;
	t_list *next;

	cur = *list;
	prev = NULL;
	while (cur)
	{
		next = cur->next;
		cur->next = prev;
		prev = cur;
		cur = next;
	}
	*list = prev;
}
